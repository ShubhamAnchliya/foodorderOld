import React from 'react'
import Menu from './Menu';
import "./Tab.css";
import { useState } from 'react';
import MenuItems from './MenuItems';
import MenuHeader from './MenuHeader';

// const allCatValues = Menu.map((curElem) =>{
//     return curElem.category; 
// })

const allCatValues = [... new Set(Menu.map((curElem) => curElem.category )), "all"];

console.log(allCatValues);
 
const GalleryReact = () => {

    const [items, setItems] = useState(Menu);

    const [catItems, setCatItems] = useState(allCatValues);

    const filterItem = (categoryItem) => {

        if (categoryItem === "all") 
        {
            setItems(Menu);
            return;

        }

        const updatedItems = Menu.filter((curElem) => {
            return curElem.category === categoryItem;
        } );
 
        setItems(updatedItems);
    }


    return (
        <>
            <h1 className="mt-3 text-center main-heading">Order Your Favourite Dish</h1>
            <hr />

            {/* menu header
            <MenuHeader filterItem={filterItem} /> */}

            {/* menu header */}
            <MenuHeader filterItem={filterItem}  catItems={catItems}  />

             {/* main item section */}
            <MenuItems  items= {items} />
           
        </>

    )
}

export default GalleryReact;